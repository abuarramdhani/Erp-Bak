        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%"><strong>CAR Type</strong><br>(Jenis CAR)</td>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%">“Claim” merujuk pada kebutuhan penggantian barang dan dibuatnya tindakan perbaikan. “Complain” merujuk pada kebutuhan dibuatnya tindakan perbaikan.</td>

    </tr>
    <tr>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%"><strong>NC (Non Conformity) Scope</strong><br>(Lingkup ketidaksesuaian/permasalahan)</td>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%">Lingkup ketidaksesuaian yang teridentifikasi.<br>
            Delivery: terkait ketepatan waktu pengiriman barang <br>
            Quality: terkait kualitas barang <br>
            Quantity: terkait jumlah barang <br>
            Other: terkait hal lain selain Delivery/Quality/Quantity <br></td>

    </tr>
    <tr>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%"><strong>Description of Fact Finding</strong><br>(Deskripsi dari fakta permsalahan yang ditemukan)</td>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%">Wajib diisi dengan fakta-fakta pendukung yang ditemukan pada saat dilakukannya verifikasi. Foto dapat dilampirkan sebagai bukti penguat.</td>

    </tr>
    <tr>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%"><strong>Rootcause Analysis</strong><br>(Analisa Akar Penyebab)</td>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%">Analisa akar penyebab wajib diisi dengan hasil penyelidikan semua faktor yang (mungkin) berkontribusi sebagai penyebab terjadinya masalah. Grafik, diagram, foto, laporan, dll, dapat dilampirkan sebagai dokumen penunjang.</td>

    </tr>
    <tr>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%"><strong>Corrective Action</strong><br>(Tindakan Perbaikan)</td>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%">Tindakan perbaikan wajib diisi dengan tindakan konkret diambil yang untuk menyelesaikan permasalahan yang muncul dan untuk menghindari permasalahan serupa terulang di kemudian hari.</td>

    </tr>
    <tr>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%"><strong>Corrective Action Plan start date</strong><br>(Tanggal Tindakan Perbaikan dimulai)</td>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%">Tanggal rencana tindakan perbaikan dimulai. Apabila ada lebih dari 1 tindakan perbaikan, isi dengan tanggal paling awal dari keseluruhan rencana.</td>

    </tr>
    <tr>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%"><strong>Corrective Action Target Completion Date</strong><br>(Tanggal Tindakan Perbaikan selesai)</td>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%">Tanggal rencana tindakan perbaikan selesai. Apabila ada lebih dari 1 tindakan perbaikan, isi dengan tanggal paling akhir dari keseluruhan rencana.</td>

    </tr>
    <tr>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%"><strong>Verification</strong><br>(Verifikasi)</td>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%">Wajib diisi dengan fakta-fakta pendukung yang ditemukan pada saat dilakukannya verifikasi. Foto, laporan, dll, dapat dilampirkan sebagai bukti penguat.</td>

    </tr>
    <tr>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%"><strong>Action effectiveness</strong><br>(Keefektifan tindakan perbaikan)</td>
        <td style="padding-left:10px;border:1px solid black;border-collapse:collapse;font-size:9pt;width:50%">Kesimpulan dari hasil verifikasi yang menentukan status penutupan CAR. CAR akan ditutup (dinyatakan selesai) apabila tindakan perbaikan dinilai efektif. CAR akan ditutup namun kemudian dibuat CAR baru apabila tindakan perbaikan dinilai masih perlu perbaikan lanjutan.</td>

    </tr>
